package hello.hellospring.domain;

public class Member {

    // 임의의 id(사용자가 정하는 id 아님)
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
